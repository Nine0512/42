#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	char	*a;

	a = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str + ('a' - 'A');
		}
		str++;
	}
	return (a);
}
