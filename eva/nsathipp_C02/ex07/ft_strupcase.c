#include <unistd.h>

char	*ft_strupcase(char *str)
{
	char	*a;

	a = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str + ('A' - 'a');
		}
		str++;
	}
	return (a);
}
