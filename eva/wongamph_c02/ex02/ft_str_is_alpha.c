#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	res;

	while (*str != '\0')
	{
		res = 0;
		if ((*str >= 'A') && (*str <= 'Z'))
		{
			res = 1;
		}
		if ((*str >= 'a') && (*str <= 'z'))
		{
			res = 1;
		}
		if (!res)
		{
			return (0);
		}
		str++;
	}
	return (1);
}
