int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while (i < 46341)
	{
		if (nb == 2 || nb < 0)
			return (0);
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
